package szp.model;

/**
 * This is the Role enumeration that represents the role of an employee in the system.
 * It can be one of the following: ADMIN, EMPLOYEE, HR.
 */
public enum Role {
    /**
     * Represents the ADMIN role.
     */
    ADMIN,

    /**
     * Represents the EMPLOYEE role.
     */
    EMPLOYEE,

    /**
     * Represents the HR role.
     */
    HR;
}